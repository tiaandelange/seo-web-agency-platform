import type { Metadata } from 'next';
import Link from 'next/link';
import { notFound } from 'next/navigation';
import { buildMetadata } from '@/lib/seo';
import { Breadcrumbs } from '@/components/breadcrumbs';
import { PageHero } from '@/components/layout/page-hero';
import { Section } from '@/components/section';
import { InkBand } from '@/components/layout/ink-band';
import { CardGrid, InfoCard } from '@/components/cards';
import { FaqList } from '@/components/faq-list';
import { CtaQuote } from '@/components/cta-quote';
import { JsonLd } from '@/components/json-ld';
import { serviceSchema, webPageSchema } from '@/lib/schema';
import { packages, getPackage } from '@/data/packages';
import { getService } from '@/data/services';
import { getPackageClarity } from '@/data/package-clarity';
import { PackageClarityGrid } from '@/components/packages/package-clarity-grid';

interface Params {
  slug: string;
}

export function generateStaticParams(): Params[] {
  return packages.map((p) => ({ slug: p.slug }));
}

export const dynamicParams = false;

export async function generateMetadata({ params }: { params: Promise<Params> }): Promise<Metadata> {
  const { slug } = await params;
  const pkg = getPackage(slug);
  if (!pkg) return {};
  return buildMetadata({
    title: pkg.title,
    seoTitle: pkg.seoTitle,
    description: pkg.metaDescription,
    path: `/website-packages/${pkg.slug}/`,
    index: !pkg.noindex,
  });
}

export default async function PackagePage({ params }: { params: Promise<Params> }) {
  const { slug } = await params;
  const pkg = getPackage(slug);
  if (!pkg) notFound();

  const path = `/website-packages/${pkg.slug}/`;
  const parentService = getService(pkg.serviceSlug);
  const clarity = getPackageClarity(pkg.slug);
  const priceLabel = pkg.priceRange
    ? `Indicative range: R${pkg.priceRange.min.toLocaleString('en-ZA')} – R${pkg.priceRange.max.toLocaleString('en-ZA')}`
    : 'Priced via paid discovery — see how that works below.';

  return (
    <>
      <Breadcrumbs path={path} />
      <PageHero
        variant="inverse"
        motif
        eyebrow="Website package"
        title={pkg.heading}
        description={pkg.intro}
        meta={
          <p className="text-sm text-sandstone">
            Updated{' '}
            {new Date(pkg.dateUpdated).toLocaleDateString('en-ZA', {
              year: 'numeric',
              month: 'long',
            })}
          </p>
        }
        aside={
          <div className="rounded-card border border-white/15 bg-ink/40 p-5 shadow-card backdrop-blur-sm">
            <p className="text-label text-cta">Indicative</p>
            <p className="mt-3 text-sm font-semibold leading-snug text-accent-contrast">{priceLabel}</p>
            <p className="mt-3 text-sm text-sandstone">Delivery: {pkg.timeline}</p>
          </div>
        }
      />

      <Section heading="Who this package fits" tone="surface">
        <CardGrid>
          {pkg.idealFor.map((item, i) => (
            <InfoCard
              key={item}
              label={String(i + 1).padStart(2, '0')}
              description={item}
              headingAs="h3"
            />
          ))}
        </CardGrid>
      </Section>

      {clarity && (
        <Section heading="Package clarity">
          <p className="mb-6 max-w-3xl leading-relaxed text-muted">
            Scope boundaries in one place — so you know what is included, who writes content, and
            what moves the indicative price.
          </p>
          <div className="overflow-hidden rounded-card border border-line shadow-card">
            <PackageClarityGrid clarity={clarity} />
          </div>
        </Section>
      )}

      <Section heading="What you get" tone="surface">
        <CardGrid>
          {pkg.inclusions.map((item, i) => (
            <InfoCard
              key={item}
              label={String(i + 1).padStart(2, '0')}
              description={item}
              headingAs="h3"
            />
          ))}
        </CardGrid>
      </Section>

      <InkBand heading="What it costs" motif>
        <p className="max-w-3xl text-xl font-semibold text-accent-contrast">{priceLabel}</p>
        <p className="mt-4 max-w-3xl leading-relaxed text-sandstone">
          Delivery timeline: {pkg.timeline}. Where you land in the range depends on scope
          decisions we make together — you receive a fixed, itemised quote after one scoping
          conversation, and the quote is the binding number.
        </p>
      </InkBand>

      <Section heading="What's excluded" tone="surface">
        <CardGrid>
          {pkg.exclusions.map((item, i) => (
            <InfoCard
              key={item}
              label={String(i + 1).padStart(2, '0')}
              description={item}
              headingAs="h3"
            />
          ))}
        </CardGrid>
      </Section>

      {parentService && (
        <Section heading="The service behind this package">
          <div className="max-w-3xl rounded-card border border-line bg-canvas p-6 shadow-card">
            <p className="leading-relaxed text-muted">
              This package is a defined scope of our{' '}
              <Link href={`/services/${parentService.slug}/`} className="text-accent underline">
                {parentService.heading}
              </Link>{' '}
              service — read that page for the full capability picture, methods and FAQs.
            </p>
          </div>
        </Section>
      )}

      {pkg.faqs.length > 0 && (
        <Section heading="Package questions" tone="surface">
          <FaqList items={pkg.faqs} variant="cards" />
        </Section>
      )}

      <CtaQuote heading={`Get a fixed quote for the ${pkg.heading.toLowerCase()}`} />
      <JsonLd
        data={[
          webPageSchema({
            path,
            title: pkg.title,
            description: pkg.metaDescription,
            dateModified: pkg.dateUpdated,
          }),
          // Service schema without Offer/price — pricing is indicative (D-11).
          ...(parentService ? [serviceSchema(parentService)] : []),
        ]}
      />
    </>
  );
}
